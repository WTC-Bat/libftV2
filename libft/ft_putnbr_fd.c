#include "libft.h"

void	ft_putnbr_fd(int i, int fd)
{
	unsigned int	n;

	if (i < 0)
	{
		ft_putchar_fd('-', fd);
		n = -i;
	}
	else
		n = i;
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd('0' + n, fd);
}
